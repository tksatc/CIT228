minusResult = num1-num2
multiplyResult = num1*num2

print("\nFirst number = ", num1)
print("Second number = ", num2)
print(num1, " / ", num2, " = ", " ", divResult) 
print(num1, " // ", num2, " = ", " ", + floorResult)
print(num1, " % ", num2, " = ", " ", modulusResult)
print(num1, " + ", num2, " = ", " ", addResult)
print(num1, " - ", num2, " = ", " ", minusResult)
print(num1, " * ", num2, " = ", " ", multiplyResult)

